package com.gs.spider.model;

public class User {
	private String dataId;
	private String title;
	private String releaseDate;
	private String releasePerson;
	private String tempSource;
	private String tempContent;
	private String tempType;
	private String tempKeyword;
	private String status;
	private String imgPath;
	private String sourceUrl;
	private String catagory;
	public String getDataId() {
		return dataId;
	}
	public void setDataId(String dataId) {
		this.dataId = dataId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getReleaseDate() {
		return releaseDate;
	}
	public void setReleaseDate(String releaseDate) {
		this.releaseDate = releaseDate;
	}
	public String getReleasePerson() {
		return releasePerson;
	}
	public void setReleasePerson(String releasePerson) {
		this.releasePerson = releasePerson;
	}
	public String getTempSource() {
		return tempSource;
	}
	public void setTempSource(String tempSource) {
		this.tempSource = tempSource;
	}
	public String getTempContent() {
		return tempContent;
	}
	public void setTempContent(String tempContent) {
		this.tempContent = tempContent;
	}
	public String getTempType() {
		return tempType;
	}
	public void setTempType(String tempType) {
		this.tempType = tempType;
	}
	public String getTempKeyword() {
		return tempKeyword;
	}
	public void setTempKeyword(String tempKeyword) {
		this.tempKeyword = tempKeyword;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getImgPath() {
		return imgPath;
	}
	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}
	public String getSourceUrl() {
		return sourceUrl;
	}
	public void setSourceUrl(String sourceUrl) {
		this.sourceUrl = sourceUrl;
	}
	public String getCatagory() {
		return catagory;
	}
	public void setCatagory(String catagory) {
		this.catagory = catagory;
	}
	
}
